package AritmatikOperator_AtamaOperator_PrefixAndPostfix_Scanner_;

public class IliskiselOperatorler {

public static void main(String[] args) {
		
		int x = 10;
		int y = 4;
		boolean sonuc;
		sonuc = (x > y);
		System.out.println("x > y =" + sonuc);
		sonuc = (x < y);
		System.out.println("x < y =" + sonuc);
		sonuc = (x <= y);
		System.out.println("x <= y =" + sonuc);
		sonuc = (x >= y);
		System.out.println("x >= y =" + sonuc);
		sonuc = (x == y);
		System.out.println("x == y =" + sonuc);
		sonuc = (x != y);
		System.out.println("x != y =" + sonuc);
	}

}

